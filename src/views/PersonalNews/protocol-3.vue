    <p>6. 计量失准及异议处理规则</p>
    <p>6.1 一方认为用电计量装置失准，有权提出校验请求，对方不得拒绝。校验应由有资质的计量检定机构实施。如供电人已为用电人提供计量装置校验服务超过三次且不属于供电人责任的，则超出部分相关费用由用电人承担。</p>
    <p>用电人在申请验表期间，其电费仍应按期交纳，验表结果确认后，再行退、补电费。</p>
    <p>6.2 计量失准时，计费差额电量按下列方式确定：</p>
    <p>（1）互感器或电能表误差超出允许范围时，以“0”误差为基准，按验证后的误差值确定计费差额电量。上述超差时间从上次校验或换装后投运之日至误差更正之日的二分之一时间计算；</p>
    <p>（2）其他非人为原因致使计量记录不准时，以用电人上年度或正常月份用电量的平均值为基准，确定计费差额电量，计算退、补电量的时间按导致失准时间至误差更正之日的差值确定。</p>
    <p>发生以上情形，退、补电量未确定之前，用电人先按抄见电量如期交纳电费，误差确定后，再行退补。</p>
    <p>6.3 以下原因导致的电能计量或计算出现差错时，计费差额电量按下列方式确定：</p>
    <p>（1）计费计量装置接线错误的，以其实际记录的电量为基数，按正确与错误接线的差额率退、补电量，计算退、补电量的时间从上次校验或换装投运之日至接线错误更正之日；</p>
    <p>（2）计算电量的计费倍率与实际倍率不符的，以实际倍率为基准，按正确与错误倍率的差值确定计费差额电量，计算退、补电量的时间以发生时间为准确定。</p>
    <p>发生以上情形，退、补电量未确定之前，用电人先按抄见电量如期交纳电费，误差确定后，再行退补。</p>
    <p>6.4 抄表记录、用电信息采集系统、表内留存的信息作为双方处理有关计量争议的依据。</p>
