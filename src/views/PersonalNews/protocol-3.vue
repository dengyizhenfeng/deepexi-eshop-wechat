    <p>15. 用电人的违约责任</p>
    <p>15.1 用电人违反本合同约定义务，应当按照国家、电力行业标准或本合同约定予以改正，并继续履行。用电人违约行为危及供电安全时，供电人可要求用电人立即改正，用电人拒不改正时，供电人可采用操作用电人设施等方式直接代替用电人改正，相关费用和损失由用电人承担。</p>
    <p>15.2 由于用电人责任造成供电人对外供电停止，应当按供电人少供电量乘以上月份平均售电单价给予赔偿；其中，少供电量为停电时间上月份每小时平均供电量乘以停电小时。停电时间不足１小时的按１小时计算，超过１小时的按实际停电时间计算。</p>
