    <p>10.3 供电人依法进行的用电检查或抄表，用电人应提供方便并予以配合，根据检查内容提供相应资料。用电检查的内容是：</p>
    <p>（1）用户受（送）电装置工程施工质量检验；</p>
    <p>（2）用户受（送）电装置中电气设备运行安全状况；</p>
    <p>（3）用电计量装置、电力负荷控制装置、继电保护和自动装置、调度通讯等安全运行状况；</p>
    <p>（4）供用电合同及有关协议履行的情况；</p>
    <p>（5）受电端电能质量状况；</p>
    <p>（6）违章用电和窃电行为；</p>
    <p>（7）并网电源、自备电源并网安全状况。</p>
