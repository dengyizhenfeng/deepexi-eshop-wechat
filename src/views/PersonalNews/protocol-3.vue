    <p>14. 供电人的违约责任</p>
    <p>14.1 供电人违反本合同约定，应当按照国家、电力行业标准或本合同约定予以改正，继续履行。</p>
    <p>14.2 供电人违反本合同电能质量义务给用电人造成损失的，应赔偿用电人实际损失，最高赔偿限额为用电人在电能质量不合格的时间段内实际用电量和对应时段的平均电价乘积的百分之二十。但因用电人原因导致供电人未能履行电能质量保证义务的，则对用电人的该部分损失，供电人不承担赔偿责任。</p>
    <p>14.3 供电人违反本合同约定实施停电给用电人造成损失的，应赔偿用电人实际损失，最高赔偿限额为用电人在停电时间内可能用电量（该用电量的计算参照）电度电费的五倍。</p>
