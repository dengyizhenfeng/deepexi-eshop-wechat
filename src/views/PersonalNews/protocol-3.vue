    <p>9. 中止供电程序</p>
    <p>9.1 因故需要中止供电的，按如下程序进行：</p>
    <p>（1）供电设施计划检修需要中止供电的，供电人应当提前7日公告停电区域、停电线路、停电时间，并通知重要电力用户等级的用电人；</p>
    <p>（2）供电设施临时检修需要中止供电的，供电企业应当提前24小时公告停电区域、停电线路、停电时间，并通知重要电力用户等级的用电人。</p>
    <p>9.2 发生以下情形之一的，供电人可当即中止供电：</p>
    <p>（1）发生不可抗力或紧急避险；</p>
    <p>（2）用电人实施本合同第13.6条至第13.11条行为的。</p>
    <p>9.3 因执行政府机关或授权机构做出的停电指令而中止供电的，供电人应按照指令的要求中止供电。</p>
    <p>9.4 除以上中止供电情形外，需对用电人中止供电时，按如下程序进行：</p>
    <p>（1）停电前三至七天内，将停电通知书送达用电人,对重要电力用户的停电，同时将停电通知书报送同级电力管理部门；</p>
    <p>（2）停电前30分钟，将停电时间再通知用电人一次。</p>
    <p>9.5 引起中止供电或限电的原因消除后，供电人应在三日内恢复供电。不能在三日内恢复供电的，应向用电人说明原因。</p>
